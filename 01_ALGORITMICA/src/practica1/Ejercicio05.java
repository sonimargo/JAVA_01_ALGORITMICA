package practica1;

// Imprimier segundos en una año
public class Ejercicio05
{
	public static void main(String[] args)
	{
    	int horas = 365 * 24;
    	int minutos = horas * 60;
    	int segundos = minutos * 60;
    	
    	System.out.println("Hay "+ horas + " horas en un año.");
    	System.out.println("Hay "+ minutos + " minutos en un año.");    	
    	System.out.println("Hay "+ segundos + " segundos en un año.");
    	
	}
}
